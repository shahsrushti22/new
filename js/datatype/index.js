//non primitive data types//
//array//
//array is a non-primitive data type that is used to store multiple values in a single variable. It is an ordered collection of elements, where each element can be of any data type, including other arrays or objects. Arrays are mutable, meaning that their elements can be changed after they are created.//
let users=["ws","ws1","ws2","ws3"];
console.log(users,typeof users)
//it shows the type of array is object but it is not an object it is a non-primitive value.//
//we cant take the similar value in the array because it is a collection of values.//
//object//
//object is a non-primitive data type that is used to store collections of key-value pairs. It is an unordered collection of properties, where each property consists of a key (also called a property name) and a value. Objects are mutable, meaning that their properties can be changed after they are created.//
let obj={
    cname:"ws",
    coursename:"javascript"
}
console.log(obj,typeof obj)
