// assignment operators//
//assignment operators are used to assign values to variables. The following are the most commonly used assignment operators in JavaScript://
//=, +=, -=, *=, /=, %=, **=//
let n=10;
console.log(n)// output is 10 because the value of n is assigned to 10//
n+=5;
console.log(n)
// output is 15 because the value of n is now 15//
//n+=5 means n=n+5, which is 10+5=15//
n-=5;
console.log(n)//ans is 10 because priviously we added 5 to n, now we are subtracting 5 from n, which is 15-5=10//
n*=5;
console.log(n)//ans is 50 because priviously we subtracted 5 from n, now we are multiplying 5 to n, which is 10*5=50//
n/=5;
console.log(n)//ans is 10 because priviously we multiplied 5 to n, now we are dividing 5 from n, which is 50/5=10//
n%=5;
console.log(n)//ans is 0 because priviously we divided 5 from n, now we are finding the remainder of n when divided by 5, which is 10%5=0//
n**=5;
console.log(n)//ans is 0 because priviously we found the remainder of n when divided by 5, now we are raising n to the power of 5, which is 0**5=0//   